 public ArrayList<Object[]> retrieveDetails(){
        
        ArrayList<Object[]> result = new ArrayList<Object[]>();
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/parkingsystem","root","");
            
            String query = "select *  from  payment join occupiedslot on PN  = PlateNumber;";
            PreparedStatement st = conn.prepareStatement(query);
           // st.setString(1, uname);
            ResultSet rs = st.executeQuery();
            while(rs.next()){
                result.add(new Object[]{
                    rs.getString("PlateNumber"),
                    rs.getString("SlotNumber"),
                    rs.getString("Fee")
                });
            }
            conn.close();
            st.close();
        }catch(SQLException ex){
            ex.printStackTrace();
        }
        return result;
    }
 private void fillTable(){
        ArrayList<Object[]> Details = retrieveDetails();
        for(Object[] Detail : Details){
            model.insertRow(jTable2.getRowCount(), new Object[]{
            Detail[0], Detail[1], Detail[2]
        });
        }
    }
 
