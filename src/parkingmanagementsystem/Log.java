    private void ReportGeneration(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReportGeneration
        {
        String path="";
        JFileChooser j=new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x=j.showSaveDialog(this);
        if(x==JFileChooser.APPROVE_OPTION)
        {
            path=j.getSelectedFile().getPath();
        }
        Document doc=new Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path+"Details.pdf"));
            doc.open();
            PdfPTable tbl=new PdfPTable(3);
            tbl.addCell("PlateNumber");
            tbl.addCell("SlotNumber");
            tbl.addCell("Fee");
            
            for(int i=0;i<jTable2.getRowCount();i++)
            {
                String n,a,m;
                n=jTable2.getValueAt(i, 0).toString();
                a=jTable2.getValueAt(i, 1).toString();
                m=jTable2.getValueAt(i, 2).toString();
                //add=jTable2.getValueAt(i, 3).toString();
               
              tbl.addCell(n);
              tbl.addCell(a);
              tbl.addCell(m);
                //tbl.addCell(add);
               
            }
            
            doc.add(tbl);
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Payment.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(Payment.class.getName()).log(Level.SEVERE, null, ex);
        }
        doc.close();
            
    }
    }//GEN-LAST:event_ReportGeneration

