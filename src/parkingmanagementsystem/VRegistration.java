    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
                // TODO add your handling code here:		
        String n=Name.getText();
        String email=PlateNumber.getText();
        String ph=PhoneNo.getText();
        String addre=Address.getText();
        String ps=Password.getText();
                int g=ph.length();
        if(n.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Name is Mandatory");
        }
        else if(email.equals(""))
        {
            JOptionPane.showMessageDialog(null, "MailID is Mandatory");
        }
        else if(ph.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Ph No is Mandatory");
        } 
        else if(g>10||g<10)
                {
                    JOptionPane.showMessageDialog(null,"Phone Numbers must contain 10 digits only");
                    
        }
        int i=1;
        try
        {
           Long.valueOf(PhoneNo.getText());
            
        }
        catch(NumberFormatException e)
        {
                PhoneNo.setText("");
                JOptionPane.showMessageDialog(this,"Phone number must contain numbers only");
                try
                {
                    Long.valueOf(PhoneNo.getText());
                    i=0;
                }
                catch(NumberFormatException e1)
         {
                PhoneNo.setText("");
                i=0;
                
                
          }
                return;
        }
        
        if(PhoneNo.getText().length()!=10)
        {
            JOptionPane.showMessageDialog(this,"Phone number is invalid and fill the phone number with 10 digits only");
            if(PhoneNo.getText().length()!=10)
            {  PhoneNo.setText("");
                //jTextField4.setText("");
            }
            else if(PhoneNo.getText().length()!=10)
            {
                PhoneNo.setText("");
            }
        }
        
       
        else if(addre.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Address is Mandatory");
        }
        else if(ps.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Password is Mandatory");
        }
        
        
        
        else
        {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/parkingsystem", "root", "")) {
            String query = "INSERT INTO Vehicle (name, email, PhoneNo, Address, Password) VALUES ( ?, ?, ?, ?, ?)";
            PreparedStatement s = con.prepareStatement(query);
            s.setString(1, n);
            s.setString(2, email);
            s.setString(3, ph);
            //s.setString(4, age);
            s.setString(4, addre);
            s.setString(5, ps);
            s.execute();
            JOptionPane.showMessageDialog(null, "Registration Successful. Your username is your email.");
            new LoginV().setVisible(true);
            this.dispose();
        } catch (SQLException ex) {
            System.out.println("Error: " + ex.getMessage());
            ex.printStackTrace();
        }
    }
        
    }//GEN-LAST:event_SubmitActionPerformed

